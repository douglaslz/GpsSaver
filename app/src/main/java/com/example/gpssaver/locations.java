package com.example.gpssaver;

public class locations {

    private String  NameLocation;
    private String  LatitudeLocation;
    private String  LongitudeLocation;

    public String getNameLocation() {
        return NameLocation;
    }

    public void setNameLocation(String name) {
        NameLocation = name;
    }

    public String getLatitudeLocation() {
        return LatitudeLocation;
    }

    public void setLatitudeLocation(String latitude) {
        LatitudeLocation = latitude;
    }

    public String getLongitudeLocation() {
        return LongitudeLocation;
    }

    public void setLongitudeLocation(String longitude) {
        LongitudeLocation = longitude;
    }



}
